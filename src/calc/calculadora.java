package calc;

public class calculadora {
	
	
	
	public double somar (int parametro1,int parametro2){
	return parametro1 + parametro2;	
	
	}
	
	public double subtrair (int parametro1 ,int parametro2){
		return parametro1 - parametro2;
	}
	
	public double multiplicar (int parametro1, int parametro2){
		return parametro1 * parametro2;
	}
	
	public double dividir (int parametro1, int parametro2){
		return parametro1 / parametro2;
	}

}
